#include <stdio.h>
#include <math.h>
#include "calc.h"

int suma(int a, int b) {
    return a + b;
}

int resta(int a, int b) {
    return a - b;
}

int multiplicacio(int a, int b) {
    return a * b;
}

double divisio(double a, double b) {
    return a / b;
}

double arrel(double a) {
    return sqrt(a);
}

int potencia(int a, int b) {
    return pow(a, b);
}